// Generates a STRUCTURE.md document from captured component HTML skeletons

export function generateStructureMd(data) {
  const { meta, components } = data;

  const h1 = meta.hostname.replace(/^www\./, '');
  const lines = [];

  lines.push(`# ${h1} Component Structure`);
  lines.push('');
  lines.push(`> Extracted from ${meta.url}`);
  lines.push('');
  lines.push('<!-- Generated by Design System Extractor Chrome Extension -->');
  lines.push('');

  const fixedSections = [
    { key: 'nav',    label: 'Navigation' },
    { key: 'hero',   label: 'Hero' },
  ];
  const tailSections = [
    { key: 'card',   label: 'Card Pattern' },
    { key: 'cta',    label: 'CTA Button' },
    { key: 'footer', label: 'Footer' },
  ];

  let capturedCount = 0;

  // nav + hero first
  for (const { key, label } of fixedSections) {
    const html = components[key];
    if (!html) continue;
    capturedCount++;
    lines.push(`## ${label}`);
    lines.push('');
    lines.push('```html');
    lines.push(html.trim());
    lines.push('```');
    lines.push('');
  }

  // middle sections — everything between hero and footer
  const walkedSections = Array.isArray(components.sections) ? components.sections : [];
  if (walkedSections.length > 0) {
    lines.push('## Page Sections');
    lines.push('');
    lines.push(`> ${walkedSections.length} section${walkedSections.length === 1 ? '' : 's'} found between hero and footer.`);
    lines.push('');

    for (const s of walkedSections) {
      capturedCount++;
      lines.push(`### Section ${s.index} — ${s.role}`);
      lines.push('');

      const meta = [];
      if (s.heading) meta.push(`**heading:** ${s.heading}`);
      if (s.subhead) meta.push(`**subhead:** ${s.subhead}`);
      meta.push(`**layout:** ${s.layout}`);
      if (s.bg && s.bg !== 'rgba(0, 0, 0, 0)') meta.push(`**bg:** \`${s.bg}\``);
      if (s.padding && s.padding !== '0px') meta.push(`**padding:** ${s.padding}`);
      if (s.gap && s.gap !== 'normal' && s.gap !== '0px') meta.push(`**gap:** ${s.gap}`);
      meta.push(`**children:** ${s.childCount}`);
      if (s.imageCount > 0) meta.push(`**images:** ${s.imageCount}`);
      for (const m of meta) lines.push(`- ${m}`);
      lines.push('');

      if (s.html) {
        lines.push('```html');
        lines.push(s.html.trim());
        lines.push('```');
        lines.push('');
      }
    }
  }

  // card / cta / footer last
  for (const { key, label } of tailSections) {
    const html = components[key];
    if (!html) continue;
    capturedCount++;
    lines.push(`## ${label}`);
    lines.push('');
    lines.push('```html');
    lines.push(html.trim());
    lines.push('```');
    lines.push('');
  }

  if (capturedCount === 0) {
    lines.push('No key components were detected on this page.');
    lines.push('');
  }

  lines.push('## Using This File');
  lines.push('');
  lines.push('Paste this file into your AI context to replicate component structure:');
  lines.push('');
  lines.push('```');
  lines.push(`I'm building a UI that matches the ${h1} component structure. Use the HTML skeletons in STRUCTURE.md as templates for navigation, hero, cards, and footer layouts.`);
  lines.push('```');
  lines.push('');

  return { markdown: lines.join('\n'), capturedCount };
}
