// Generates a DESIGN.md document from extracted design system data

export function generateDesignMd(data) {
  const { meta, cssVars, classified, stylesheetData, framework, fonts, components, spacing } = data;

  const lines = [];
  const h1 = meta.hostname.replace(/^www\./, '');

  // ── Header ────────────────────────────────────────────────────────────────
  lines.push(`# ${h1} Design System`);
  lines.push(`> Extracted ${meta.timestamp.slice(0, 10)} from ${meta.url}`);
  lines.push('');
  lines.push('<!-- Generated by Design System Extractor Chrome Extension -->');
  lines.push('');

  // ── Detected Framework ────────────────────────────────────────────────────
  lines.push('## Detected Framework');
  lines.push('');
  lines.push(framework.primary);
  lines.push('');
  const scoreEntries = Object.entries(framework.scores)
    .filter(([, v]) => v > 0)
    .sort(([, a], [, b]) => b - a);
  if (scoreEntries.length) {
    lines.push('**Detection confidence:**');
    scoreEntries.forEach(([k, v]) => lines.push(`- ${k}: ${v}`));
  }
  lines.push('');

  // ── Design Tokens — CSS Custom Properties ─────────────────────────────────
  lines.push('## Design Tokens (CSS Custom Properties)');
  lines.push('');

  const totalVars = Object.keys(cssVars).length;
  lines.push(`${totalVars} CSS custom properties found.`);
  lines.push('');

  // Colors
  const colorVars = Object.entries(classified.colors);
  if (colorVars.length) {
    lines.push('### Colors');
    lines.push('');
    colorVars.forEach(([name, data]) => {
      const resolved = data.resolvedValue ? ` → resolved: \`${data.resolvedValue}\`` : '';
      lines.push(`- \`${name}: ${data.value}\`${resolved}`);
    });
    lines.push('');
  }

  // Typography tokens
  const typographyVars = Object.entries(classified.typography);
  if (typographyVars.length) {
    lines.push('### Typography Tokens');
    lines.push('');
    typographyVars.forEach(([name, data]) => {
      lines.push(`- \`${name}: ${data.value}\``);
    });
    lines.push('');
  }

  // Spacing tokens
  const spacingVars = Object.entries(classified.spacing);
  if (spacingVars.length) {
    lines.push('### Spacing Tokens');
    lines.push('');
    spacingVars.forEach(([name, data]) => {
      lines.push(`- \`${name}: ${data.value}\``);
    });
    lines.push('');
  }

  // Border radius tokens
  const radiusVars = Object.entries(classified.radius);
  if (radiusVars.length) {
    lines.push('### Border Radius Tokens');
    lines.push('');
    radiusVars.forEach(([name, data]) => {
      lines.push(`- \`${name}: ${data.value}\``);
    });
    lines.push('');
  }

  // Shadow tokens
  const shadowVars = Object.entries(classified.shadow);
  if (shadowVars.length) {
    lines.push('### Shadow Tokens');
    lines.push('');
    shadowVars.forEach(([name, data]) => {
      lines.push(`- \`${name}: ${data.value}\``);
    });
    lines.push('');
  }

  // Animation tokens
  const animationVars = Object.entries(classified.animation);
  if (animationVars.length) {
    lines.push('### Animation Tokens');
    lines.push('');
    animationVars.forEach(([name, data]) => {
      lines.push(`- \`${name}: ${data.value}\``);
    });
    lines.push('');
  }

  // Other tokens
  const otherVars = Object.entries(classified.other);
  if (otherVars.length) {
    lines.push('### Other Tokens');
    lines.push('');
    otherVars.slice(0, 50).forEach(([name, data]) => {
      lines.push(`- \`${name}: ${data.value}\``);
    });
    if (otherVars.length > 50) lines.push(`- ... and ${otherVars.length - 50} more`);
    lines.push('');
  }

  // ── Typography ────────────────────────────────────────────────────────────
  lines.push('## Typography');
  lines.push('');

  // Loaded fonts
  const loadedFamilies = fonts.loaded.map(f => f.family);
  const uniqueLoaded = [...new Set(loadedFamilies)];
  if (uniqueLoaded.length) {
    lines.push(`**Loaded fonts:** ${uniqueLoaded.join(', ')}`);
    lines.push('');
  }

  // Google Fonts
  if (fonts.googleFonts.length) {
    lines.push(`**Google Fonts:** ${fonts.googleFonts.join(', ')}`);
    lines.push('');
  }

  // External sources
  if (fonts.externalSources.length) {
    lines.push('**External font sources:**');
    fonts.externalSources.forEach(s => lines.push(`- ${s.provider}: \`${s.url.slice(0, 80)}\``));
    lines.push('');
  }

  // Declared families (fallback stacks)
  if (fonts.declaredFamilies.length) {
    const systemFonts = ['system-ui', 'sans-serif', 'serif', 'monospace', '-apple-system',
      'BlinkMacSystemFont', 'Helvetica Neue', 'Arial', 'Segoe UI', 'Roboto', 'Ubuntu',
      'Noto Sans', 'Liberation Sans', 'Cantarell', 'Fira Sans', 'Droid Sans', 'cursive'];
    const brandFonts = fonts.declaredFamilies.filter(f => !systemFonts.some(s => f.toLowerCase().includes(s.toLowerCase())));
    if (brandFonts.length) {
      lines.push(`**Brand font families:** ${[...new Set(brandFonts)].join(', ')}`);
      lines.push('');
    }
  }

  // Font scale from stylesheet
  if (stylesheetData.fontSizes.length) {
    lines.push(`**Font size scale:** ${stylesheetData.fontSizes.join(', ')}`);
    lines.push('');
  }

  // ── Spacing Scale ─────────────────────────────────────────────────────────
  lines.push('## Spacing Scale');
  lines.push('');
  if (spacing.detectedValues.length) {
    lines.push(`**Computed scale:** ${spacing.detectedValues.join(', ')}`);
    lines.push('');
  }
  if (spacing.spacingVars.length) {
    lines.push('**Spacing tokens:**');
    spacing.spacingVars.forEach(([name, data]) => {
      lines.push(`- \`${name}: ${data.value}\``);
    });
    lines.push('');
  }

  // ── Colors from Stylesheets ───────────────────────────────────────────────
  if (stylesheetData.colors.length) {
    lines.push('## Colors (from stylesheet rules)');
    lines.push('');
    const uniqueColors = [...new Set(stylesheetData.colors)].slice(0, 60);
    uniqueColors.forEach(c => lines.push(`- \`${c}\``));
    if (stylesheetData.colors.length > 60) lines.push(`- ... and ${stylesheetData.colors.length - 60} more`);
    lines.push('');
  }

  // ── Border Radius ─────────────────────────────────────────────────────────
  if (stylesheetData.borderRadii.length) {
    lines.push('## Border Radius Scale');
    lines.push('');
    lines.push(stylesheetData.borderRadii.join(', '));
    lines.push('');
  }

  // ── Shadows ───────────────────────────────────────────────────────────────
  if (stylesheetData.shadows.length) {
    lines.push('## Shadows');
    lines.push('');
    stylesheetData.shadows.slice(0, 10).forEach(s => lines.push(`- \`${s}\``));
    lines.push('');
  }

  // ── Component Inventory ───────────────────────────────────────────────────
  lines.push('## Component Inventory');
  lines.push('');

  const compSummary = buildComponentSummary(components);
  if (compSummary.length === 0) {
    lines.push('No standard component boundaries detected.');
  } else {
    compSummary.forEach(line => lines.push(line));
  }
  lines.push('');

  // ── Animations ────────────────────────────────────────────────────────────
  if (stylesheetData.transitions.length) {
    lines.push('## Transitions');
    lines.push('');
    [...new Set(stylesheetData.transitions)].slice(0, 10).forEach(t => lines.push(`- \`${t}\``));
    lines.push('');
  }

  // ── Z-Index Scale ────────────────────────────────────────────────────────
  if (stylesheetData.zIndices.length) {
    lines.push('## Z-Index Scale');
    lines.push('');
    lines.push(stylesheetData.zIndices.join(', '));
    lines.push('');
  }

  // ── Full CSS Variables Dump ───────────────────────────────────────────────
  lines.push('## Raw CSS Variables (full dump)');
  lines.push('');
  lines.push('```css');
  lines.push(':root {');

  const allVarsSorted = Object.entries(cssVars).sort(([a], [b]) => a.localeCompare(b));
  allVarsSorted.forEach(([name, data]) => {
    lines.push(`  ${name}: ${data.value};`);
  });
  lines.push('}');
  lines.push('```');
  lines.push('');

  // ── AI Prompt Usage ───────────────────────────────────────────────────────
  lines.push('## Using This File');
  lines.push('');
  lines.push('Paste this file into your AI context to replicate this design system:');
  lines.push('');
  lines.push('```');
  lines.push(`I'm building a UI that matches the ${h1} design system. Use the tokens in DESIGN.md for all colors, typography, spacing, and component styles.`);
  lines.push('```');
  lines.push('');

  return lines.join('\n');
}

function buildComponentSummary(components) {
  const lines = [];

  if (components.buttons.length) {
    const variants = [...new Set(components.buttons.map(b => b.variant))].join(', ');
    lines.push(`- **${components.buttons.length} buttons** — variants: ${variants}`);
  }
  if (components.dialogs.length) {
    const visible = components.dialogs.filter(d => d.visible).length;
    lines.push(`- **${components.dialogs.length} dialogs/modals** — ${visible} visible`);
  }
  if (components.forms.length) {
    const totalInputs = components.forms.reduce((s, f) => s + f.inputs, 0);
    lines.push(`- **${components.forms.length} forms** — ${totalInputs} input fields total`);
  }
  if (components.inputs.length) {
    const types = [...new Set(components.inputs.map(i => i.type))].join(', ');
    lines.push(`- **${components.inputs.length} inputs** — types: ${types}`);
  }
  if (components.navs.length) {
    lines.push(`- **${components.navs.length} navigation regions**`);
  }
  if (components.cards.length) {
    lines.push(`- **${components.cards.length} cards**`);
  }
  if (components.alerts.length) {
    lines.push(`- **${components.alerts.length} alerts/notifications**`);
  }
  if (components.tables.length) {
    const rows = components.tables.reduce((s, t) => s + t.rows, 0);
    lines.push(`- **${components.tables.length} tables** — ${rows} rows total`);
  }
  if (components.badges.length) {
    lines.push(`- **${components.badges.length} badges/chips/tags**`);
  }
  if (components.dropdowns.length) {
    lines.push(`- **${components.dropdowns.length} dropdowns/select menus**`);
  }

  return lines;
}
